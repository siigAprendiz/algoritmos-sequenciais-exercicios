"""
Escreva um programa que solicite 2 valores informados pelo usuário.
O programa deve calcular e exibir:
A soma dos dois valores
A multiplicação dos dois valores
"""

primeiroNumero = float(input("Digite um número qualquer: "))
segundoNumero = float(input("Agora, digite outro número qualquer: "))

soma = primeiroNumero + segundoNumero
multiplicacao = primeiroNumero * segundoNumero

print(f"A soma dos dois valores é {soma}")
print(f"A multiplicação dos dois valores é {multiplicacao}")
